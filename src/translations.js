/** Supported UI language codes. Unsupported → fallback to English. */
const SUPPORTED_LANGS = ['en', 'fr', 'de', 'hi', 'es'];

/**
 * Get translation string for a key in the given language.
 * Use for dynamic messages (e.g. policy status) so they match the current UI language.
 * @param {string} languageCode - e.g. 'en', 'es', 'fr', 'de', 'hi'
 * @param {string} key - translation key (e.g. 'policyDetected', 'privacy_policy_detected')
 * @returns {string}
 */
function getTranslation(languageCode, key) {
  const code = (languageCode || 'en').toLowerCase().slice(0, 2);
  const t = TRANSLATIONS[code] || TRANSLATIONS.en;
  return (t && t[key]) != null ? t[key] : (TRANSLATIONS.en[key] || key);
}

/**
 * Get translated message with placeholders {0}, {1}, ... replaced by args.
 * @param {string} languageCode - e.g. 'en', 'es'
 * @param {string} key - translation key
 * @param {...string} args - values to replace {0}, {1}, ...
 * @returns {string}
 */
function getMessage(languageCode, key) {
  let s = getTranslation(languageCode, key);
  for (let i = 0; i < arguments.length - 2; i++) {
    s = s.replace(new RegExp('\\{' + i + '\\}', 'g'), arguments[i + 2]);
  }
  return s;
}

/**
 * Apply UI language to the current document (popup or dashboard).
 * Only updates text content; does not change behavior or logic.
 * @param {string} languageCode - e.g. 'en', 'es', 'fr', 'de', 'hi'
 */
function applyUILanguage(languageCode) {
  const code = (languageCode || 'en').toLowerCase().slice(0, 2);
  const t = SUPPORTED_LANGS.includes(code) ? TRANSLATIONS[code] : TRANSLATIONS.en;
  if (!t) return;

  const doc = typeof document !== 'undefined' ? document : null;
  if (!doc || !doc.body) return;

  function setText(el, text, withSpace) {
    if (!el || text == null) return;
    if (withSpace && el.lastChild && el.lastChild.nodeType === Node.TEXT_NODE) {
      el.lastChild.textContent = (text && ' ' + text) || '';
    } else {
      el.textContent = text != null ? text : '';
    }
  }

  function setPlaceholder(el, text) {
    if (el && text != null) el.setAttribute('placeholder', text);
  }

  // --- Popup elements (by id or selector) ---
  const popupMap = [
    { id: 'openDashboard', key: 'dashboard', icon: true },
    { selector: '.score-card .score-header .label', key: 'privacyScore' },
    { id: 'scoreLabel', key: 'analyzing' },
    { selector: '#scoreBreakdown .breakdown-item:nth-child(1) .breakdown-label', key: 'policyRisk' },
    { selector: '#scoreBreakdown .breakdown-item:nth-child(2) .breakdown-label', key: 'permissions' },
    { selector: '#scoreBreakdown .breakdown-item:nth-child(3) .breakdown-label', key: 'cookies' },
    { selector: '.card:nth-of-type(2) .section-header .label', key: 'liveActivityFeed', icon: true },
    { id: 'clearFeed', key: 'clear', icon: true },
    { selector: '.card:nth-of-type(3) .section-header .label', key: 'cookieDataControl', icon: true },
    { selector: '.cookie-info', key: 'cookiesOnSite', suffix: true },
    { id: 'clearCookies', key: 'clearAll', icon: true },
    { id: 'blockCookies', key: 'blockCookies', icon: true },
    { selector: '.permission-card:nth-child(1) .permission-name', key: 'location' },
    { selector: '.permission-card:nth-child(2) .permission-name', key: 'notifications' },
    { selector: '.permission-card:nth-child(3) .permission-name', key: 'camera' },
    { selector: '.permission-card:nth-child(4) .permission-name', key: 'microphone' },
    { selector: '.card:has(#policyStatus) .section-header .label', key: 'privacyPolicyAnalyzer', icon: true },
    { id: 'policyStatus', key: 'detecting' },
    { id: 'fetchPolicy', key: 'fetchPolicy', icon: true },
    { id: 'analyzePolicy', key: 'analyzeAI', icon: true },
    { id: 'exportSummary', key: 'export', icon: true },
    { selector: '#policyPreview .preview-header span', key: 'policyPreview' },
    { selector: '#policySummary .summary-header span', key: 'aiAnalysisSummary' },
    { selector: '#policyTranslationSection .preview-header span', key: 'originalVsTranslated' },
    { selector: '.card:has(#autoView) .section-header .label', key: 'smartAutomation', icon: true },
    { id: 'automationStatus', key: 'learningPreferences' },
    { id: 'autoView', key: 'viewRules', icon: true },
    { id: 'autoReset', key: 'resetLearning', icon: true },
    { selector: '.card:has(#clearLog) .section-header .label', key: 'liveLog', icon: true },
    { id: 'clearLog', key: 'clear', icon: true }
  ];

  const taglineEl = doc.querySelector('.hero .tag');
  if (taglineEl) taglineEl.textContent = t.tagline || '';

  for (const item of popupMap) {
    const el = item.id ? doc.getElementById(item.id) : doc.querySelector(item.selector);
    if (!el) continue;
    const text = t[item.key];
    if (item.selector && item.selector.includes('#policyStatus') && el.id === 'policyStatus') {
      setText(el, text, false);
      continue;
    }
    if (item.suffix && el.querySelector('.cookie-count')) {
      const countEl = el.querySelector('.cookie-count');
      const count = (countEl && countEl.textContent) || '0';
      const id = countEl && countEl.id ? countEl.id : '';
      el.innerHTML = '<span class="cookie-count"' + (id ? ' id="' + id + '"' : '') + '>' + count + '</span> ' + (text || '');
      continue;
    }
    setText(el, text, item.icon);
  }

  // Popup labels inside buttons that are "Icon + Text" (first child icon, last child text)
  const allowBlock = [
    { id: 'geoAllow', key: 'allow' }, { id: 'geoBlock', key: 'block' },
    { id: 'notifAllow', key: 'allow' }, { id: 'notifBlock', key: 'block' },
    { id: 'cameraAllow', key: 'allow' }, { id: 'cameraBlock', key: 'block' },
    { id: 'micAllow', key: 'allow' }, { id: 'micBlock', key: 'block' }
  ];
  for (const { id, key } of allowBlock) {
    const el = doc.getElementById(id);
    if (el && el.lastChild) setText(el, t[key], true);
  }

  // --- Dashboard elements ---
  const dashTitle = doc.querySelector('.dashboard-title');
  if (dashTitle) setText(dashTitle, t.dashboardTitle, true);
  const dashSub = doc.querySelector('.dashboard-subtitle');
  if (dashSub) dashSub.textContent = t.dashboardSubtitle || '';

  const tabKeys = {
    'overview': 'overview',
    'monitoring': 'liveMonitoring',
    'analyzer': 'policyAnalyzer',
    'preferences': 'smartPreferences',
    'consent': 'consentLog',
    'gdpr': 'gdprControls'
  };
  for (const [dataTab, key] of Object.entries(tabKeys)) {
    const tab = doc.querySelector('.tab-item[data-tab="' + dataTab + '"]');
    if (tab) setText(tab, t[key], true);
  }

  const dashMap = [
    { selector: '#overview h2', key: 'privacyOverview' },
    { selector: '#overview .stat-label', key: 'sitesTracked', index: 0 },
    { selector: '#overview .stat-label', key: 'avgPrivacyScore', index: 1 },
    { selector: '#overview .stat-label', key: 'totalCookies', index: 2 },
    { selector: '#overview .stat-label', key: 'highRiskSites', index: 3 },
    { selector: '#overview .section-header .label', key: 'recentActivity', icon: true },
    { id: 'clearActivity', key: 'clear', icon: true },
    { selector: '#monitoring .table-title', key: 'sitesPermissions', icon: true },
    { id: 'siteSearch', key: 'searchSites', placeholder: true },
    { selector: '#monitoring .btn-primary.btn-sm', key: 'addCurrent', icon: true },
    { selector: '#monitoring .btn-secondary.btn-sm', key: 'export', icon: true },
    { selector: '#analyzer h2', key: 'policyAnalyzer' },
    { selector: '#analyzer .section-header .label', key: 'aiPolicyAnalysis', icon: true },
    { id: 'analyzeCurrentSite', key: 'analyzeCurrentSite', icon: true },
    { id: 'analyzeAllSites', key: 'analyzeAllSites', icon: true },
    { id: 'exportAnalysis', key: 'exportAnalysis', icon: true },
    { selector: '#preferences h2', key: 'smartPreferences' },
    { selector: '#preferences .section-header .label', key: 'smartAutomationLabel', icon: true },
    { selector: '#preferences .preference-description', key: 'autoApplyHint' },
    { selector: '#consent h2', key: 'consentLog' },
    { selector: '#consent .section-header .label', key: 'privacyActionsHistory', icon: true },
    { id: 'logSearch', key: 'searchActions', placeholder: true },
    { id: 'exportToWord', key: 'exportToWord', icon: true },
    { id: 'clearLog', key: 'clearLog', icon: true },
    { selector: '#gdpr h2', key: 'gdprPrivacyControls' },
    { selector: '#gdpr .gdpr-card:nth-child(1) .gdpr-title', key: 'requestMyData' },
    { selector: '#gdpr .gdpr-card:nth-child(1) .gdpr-description', key: 'exportDataDesc' },
    { selector: '#gdpr .gdpr-card:nth-child(1) .btn', key: 'exportData', icon: true },
    { selector: '#gdpr .gdpr-card:nth-child(2) .gdpr-title', key: 'eraseMyData' },
    { selector: '#gdpr .gdpr-card:nth-child(2) .gdpr-description', key: 'eraseDataDesc' },
    { selector: '#gdpr .gdpr-card:nth-child(2) .btn', key: 'eraseAllData', icon: true },
    { selector: '#gdpr .gdpr-card:nth-child(3) .gdpr-title', key: 'privacySettings' },
    { selector: '#gdpr .gdpr-card:nth-child(3) .gdpr-description', key: 'settingsDesc' },
    { selector: '#gdpr .gdpr-card:nth-child(3) .btn', key: 'settings', icon: true },
    { selector: '#gdpr .gdpr-card:nth-child(4) .gdpr-title', key: 'dataTransparency' },
    { selector: '#gdpr .gdpr-card:nth-child(4) .gdpr-description', key: 'viewDataDesc' },
    { selector: '#gdpr .gdpr-card:nth-child(4) .btn', key: 'viewData', icon: true },
    { selector: '#gdpr .card .section-header .label', key: 'privacyInformation', icon: true }
  ];

  for (const item of dashMap) {
    const el = item.id ? doc.getElementById(item.id) : (item.index != null
      ? doc.querySelectorAll(item.selector)[item.index]
      : doc.querySelector(item.selector));
    if (!el) continue;
    const text = t[item.key];
    if (item.placeholder) {
      setPlaceholder(el, text);
    } else {
      setText(el, text, item.icon);
    }
  }

  // Persist chosen UI language for next open (optional)
  try {
    if (typeof chrome !== 'undefined' && chrome.storage && chrome.storage.local) {
      chrome.storage.local.set({ consentSyncUILang: code });
    }
  } catch (e) {}
}
